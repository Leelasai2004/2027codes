#include <bits/stdc++.h>
using namespace std;
const long long mod = (long long)1e9 + 7;
#define int long long
signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int n,k;
    cin>>n>>k;
    vector<int> v(n);
    for(int i=0;i<n;i++){
        cin>>v[i];
    }
    int l=1,r=1e18;
    int ans=*max_element(v.begin(),v.end());
    while(l<=r){
        int mid=(l-(l-r)/2);
        bool ok=true;
        int p=k;
        for(int i=0;i<n && ok;i++){
            if(v[i]<=mid) continue;
            int moves=(v[i]+mid-1)/mid;
            p-=(moves-1);
            if(p<0) ok=false;
        }
        if(ok){
            ans=mid;
            r=mid-1;
        }
        else{
            l=mid+1;
        }
    }
    cout<<ans<<'\n';
}
