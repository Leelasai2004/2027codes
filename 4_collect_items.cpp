#include <bits/stdc++.h>
using namespace std;
const long long mod = (long long)1e9 + 7;
#define int long long
signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin>>t;
    while(t--){
        int n;
        cin>>n;
        vector<pair<int,int>> v(n);
        for(int i=0;i<n;i++) cin>>v[i].first;
        for(int i=0;i<n;i++) v[i].second=i;
        sort(v.begin(),v.end());
        vector<int> ans(n);
        ans[v[n-1].second]=n-1;
        vector<int> prefix(n);
        for(int i=0;i<n;i++){
            if(i) prefix[i]+=prefix[i-1];
            prefix[i]+=v[i].first;
        }
        for(int i=n-2;i>=0;i--){
            if(prefix[i]>=v[i+1].first){
                ans[v[i].second]=ans[v[i+1].second];
            }
            else{
                ans[v[i].second]=i;
            }
        }
        for(int i=0;i<n;i++){
            cout<<ans[i]<<" \n"[i==n-1];
        }
    }
}
