#include <bits/stdc++.h>
using namespace std;

int main() {
    string s;
    cin >> s;

    vector<int> cnt(26, 0);
    vector<bool> vis(26, false);
    stack<char> st;

    for (char c : s) cnt[c - 'a']++;

    for (char c : s) {
        cnt[c - 'a']--;
        if (!vis[c - 'a']) {
            while (!st.empty() && st.top() < c && cnt[st.top() - 'a'] > 0) {
                vis[st.top() - 'a'] = false;
                st.pop();
            }
            st.push(c);
            vis[c - 'a'] = true;
        }
    }

    string res = "";
    while (!st.empty()) {
        res += st.top();
        st.pop();
    }

    reverse(res.begin(), res.end());
    cout << res << '\n';
    return 0;
}
